function add(a, b) {
    return a + b
}

function subtract (a, b) {
    let c = a - b;
    return c
}

let multiply = (a, b) => {
    return a * b
}

let divide = (a, b) => {
    return a / b
}

let increment = (n) => {
    // n++
    return ++n
}

let decrement = (n) => {
    n--
    return n
}

function makeInt(n){
     return parseInt(n, 10)

}

function preserveDecimal(n){
    return parseFloat(n)
}

